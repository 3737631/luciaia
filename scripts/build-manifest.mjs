import fs from "fs";
import path from "path";

const girlsDir = "public/girls";
const manifest = {};

for (const dir of fs.readdirSync(girlsDir)) {
  const fullPath = path.join(girlsDir, dir);
  if (!fs.statSync(fullPath).isDirectory()) continue;
  const files = fs.readdirSync(fullPath).filter(f => f.endsWith(".jpg"));
  manifest[dir] = files.map(f => f.replace(/\.jpg$/, ""));
}

const ts = `// Auto-generated by scripts/build-manifest.mjs
export const IMAGE_MANIFEST: Record<string, string[]> = ${JSON.stringify(manifest, null, 2)};
`;

fs.writeFileSync("src/lib/image-manifest.ts", ts);
console.log("Wrote src/lib/image-manifest.ts");
console.log(`\nStats:`);
for (const [id, combos] of Object.entries(manifest)) {
  console.log(`  ${id}: ${combos.length} images`);
}
